#include <stdio.h>
#include <stdlib.h>

/* Write a program that assigns the digit of the maximum 5-digit number to an array and prints it to the screen. */

int main(int argc, char *argv[]) {
	
	int i;
	
	
	int a[i];
	
	
	printf("Please enter your array:\n");
	scanf("%d",&a[i]);
	
	
	for (i=0;i<=5;i++){
		printf("%d",a[i]);
		break;
	}
			
	

		
	
	
	return 0;
}
